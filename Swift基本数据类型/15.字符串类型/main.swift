import Foundation

// swift 字符串
let s = "yanwanfu"
print(s)

// Object-C 字符串
let s1:NSString = "NSString"
print(s1)

// 把Object-C字符串转换为swift字符串，使用as关键字
let s2 = s1 as String
print(s2)

// 把swift字符串转换为Object-C字符串，使用as关键字
let s3 = s as NSString
print(s3)

